package com.example.awesomechatbot;

public class LoginResult {
    private String id;
    private String number;
    private String password;
    private String age;

    public String getId() {
        return id;
    }

    public String getNumber() {
        return number;
    }

    public String getPassword() {
        return password;
    }

    public String getAge() {
        return age;
    }
}
